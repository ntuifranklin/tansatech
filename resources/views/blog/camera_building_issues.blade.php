                                                      </li>
                                                      <li>
                                                        <h4>Inspect Hardware:</h4> Look for any signs of physical damage or environmental factors affecting the camera.
                                                      </li>
                                                      <li>
                                                        <h4>Test with Another Device:</h4> If possible, test the camera on a different power source or network to isolate the issue.

